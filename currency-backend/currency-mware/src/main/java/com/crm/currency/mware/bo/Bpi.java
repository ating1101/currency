package com.crm.currency.mware.bo;

public class Bpi {
	private CurrencyExchangeInfo USD;
	private CurrencyExchangeInfo GBP;
	private CurrencyExchangeInfo EUR;
	public CurrencyExchangeInfo getUSD() {
		return USD;
	}
	public void setUSD(CurrencyExchangeInfo uSD) {
		USD = uSD;
	}
	public CurrencyExchangeInfo getGBP() {
		return GBP;
	}
	public void setGBP(CurrencyExchangeInfo gBP) {
		GBP = gBP;
	}
	public CurrencyExchangeInfo getEUR() {
		return EUR;
	}
	public void setEUR(CurrencyExchangeInfo eUR) {
		EUR = eUR;
	}
	
	
	
}
